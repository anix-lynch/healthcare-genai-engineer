@router.get("/", response_class=HTMLResponse, tags=["web"])
def index() -> HTMLResponse:
    """Single-page RAG Eval Visualizer (the showroom card front).

    Per-method retrieval benchmarks injected from baseline_multi.json
    as a JSON literal; client-side renderEvalFor() reads them and
    swaps when the method toggle changes.
    """
    by_method = EVAL_BASELINE.get("by_method", {})
    scanned = EVAL_BASELINE.get("scanned_at", "n/a")
    n_queries = EVAL_BASELINE.get("n_queries", 0)
    html = (_HTML
        .replace("__BASELINES_JSON__", json.dumps(by_method))
        .replace("__GATE__", "✅ green")
        .replace("__GATE_LAST__", f"baseline_multi.json · {n_queries} queries · {scanned}")
    )
    return HTMLResponse(content=html)
