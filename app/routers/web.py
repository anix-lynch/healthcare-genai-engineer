def _load_eval_baseline() -> dict:
    """Read per-method retrieval benchmarks from baseline_multi.json.

    If the file is missing (e.g., first boot before eval has run),
    fall back to placeholder zeros so the page still renders.
    """
    p = Path(__file__).resolve().parents[2] / "evaluation" / "baseline_multi.json"
    if p.exists():
        try:
            return json.loads(p.read_text())
        except json.JSONDecodeError:
            pass
    return {
        "scanned_at": "n/a",
        "n_queries": 0,
        "by_method": {
            m: {"aggregates": {"hit_at_5": 0, "precision_at_5": 0,
                               "mrr": 0, "ndcg_at_10": 0, "p95_latency_ms": 0}}
            for m in ("bm25", "dense", "hybrid")
        },
    }


# Read once at startup. Re-run `python -m evaluation.multi_method_eval`
# to refresh the file; the container needs a redeploy to pick up changes.
