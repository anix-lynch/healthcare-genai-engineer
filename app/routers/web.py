from pathlib import Path
from fastapi import APIRouter
from fastapi.responses import HTMLResponse

router = APIRouter()


def _load_eval_baseline() -> dict:
    """Read per-method retrieval benchmarks from baseline_multi.json."""
    p = Path(__file__).resolve().parents[2] / "evaluation" / "baseline_multi.json"
    if p.exists():
        try:
            return json.loads(p.read_text())
        except json.JSONDecodeError:
            pass
    return {
        "scanned_at": "n/a", "n_queries": 0,
        "by_method": {
            m: {"aggregates": {"hit_at_5": 0, "precision_at_5": 0,
                               "mrr": 0, "ndcg_at_10": 0, "p95_latency_ms": 0}}
            for m in ("bm25", "dense", "hybrid")
        },
    }


def _load_classify_baseline() -> dict:
    """Read ESI classification benchmarks from classify_baseline.json."""
    p = Path(__file__).resolve().parents[2] / "evaluation" / "classify_baseline.json"
    if p.exists():
        try:
            return json.loads(p.read_text())
        except json.JSONDecodeError:
            pass
    return {
        "holdout_n": 0, "baseline_majority_class": 0,
        "by_strategy": {
            s: {"accuracy": 0, "macro_f1": 0,
                "safety_critical_recall_esi_1_and_2": 0}
            for s in ("rule_only", "rag_knn", "fused")
        },
    }


